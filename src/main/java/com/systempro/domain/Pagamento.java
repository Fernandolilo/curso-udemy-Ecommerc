package com.systempro.domain;

import java.io.Serializable;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Inheritance;
import javax.persistence.InheritanceType;
import javax.persistence.JoinColumn;
import javax.persistence.MapsId;
import javax.persistence.OneToOne;

import com.fasterxml.jackson.annotation.JsonIgnore;
import com.fasterxml.jackson.annotation.JsonTypeInfo;
import com.systempro.domain.enums.EstadoPagamento;

@Entity
//metodo para tratamento de tipo de sub class para instaciar um pagamento, 
//no corpo sa inserção sera indicado um @type, 
//apontado para qual tipo de pagamento que será feito
@JsonTypeInfo(use = JsonTypeInfo.Id.NAME, include = JsonTypeInfo.As.PROPERTY, property = "@type")
//mapeamento de herança neste caso não será gerado o tabelão, será gerado uma tabela para cada sub class
@Inheritance(strategy = InheritanceType.JOINED)
public abstract class Pagamento implements Serializable {
	private static final long serialVersionUID = 1L;

	@Id
	private Integer id;
	private Integer estado;

	// relacionamento de um para um @OneToOne, entre pagamento e pedido,
	// detro de pagamento será apresentado o campo pedido_id => @JoinColumn(name =
	// "pedido_id")
	// para garantir que i ID de pedido e pagamento sejam o mesmo e colocado esta
	// anotação => @MapsId

	// estamos fazendo a proteção de serialização ciclica, @JsonBackReference
	// o clinte não irar serializar pedido, apenas os pedidos buscará seus cliente.
	// estamos retirando a anotação @JsonBackReference e substituindo por
	// @JsonIgnore, devido ter se mostrado mais eficiente, na hora de serializar um
	// atributo.

	@JsonIgnore
	@OneToOne
	@JoinColumn(name = "pedido_id")
	@MapsId
	private Pedido pedido;

	public Pagamento() {
	}

	public Pagamento(Integer id, EstadoPagamento estado, Pedido pedido) {
		super();
		this.id = id;
		this.estado = (estado == null) ? null : estado.getCod();
		this.pedido = pedido;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Pagamento other = (Pagamento) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public EstadoPagamento getEstado() {
		return EstadoPagamento.toEnum(estado);
	}

	public void setEstado(EstadoPagamento estado) {
		this.estado = estado.getCod();
	}

	public Pedido getPedido() {
		return pedido;
	}

	public void setPedido(Pedido pedido) {
		this.pedido = pedido;
	}

}
